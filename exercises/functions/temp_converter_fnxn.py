def celsius_to_fahrenheit(celsius):
    fahrenheit = (celsius) * 1.8 + 32
    print(f"{celsius}°C is {fahrenheit}°F")


celsius_to_fahrenheit(0)
celsius_to_fahrenheit(100)
celsius_to_fahrenheit(22)
